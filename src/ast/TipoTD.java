package ast;

/**
 * L'enumerazione TipoTD definisce i tipi di dato utilizzati nel linguaggio.
 * 
 * Questa enumerazione include i seguenti tipi:
 * - INT: il tipo intero.
 * - FLOAT: il tipo floating point.
 * - OK: utilizzato per indicare un'esecuzione corretta o un'espressione corretta.
 * - ERROR: utilizzato per indicare un errore o un'espressione non corretta.
 * 
 * @author Guido Broglio 20043973
 */
public enum TipoTD 
{
    INT, FLOAT, OK, ERROR
}
